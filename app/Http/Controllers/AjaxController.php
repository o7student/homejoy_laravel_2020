    public function getservice(Request $request)
    {
        $data = Service::where('category_id','=',$request->get('category_id'))->where('status','=','ACTIVE')->get();
        $output = "";
        foreach ($data as $d) {
            $output .= '<option value="'.$d->id.'">'.$d->name.'</option>';
        }
        return response()->json($output);
    }
