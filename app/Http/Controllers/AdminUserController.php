    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        if(!session('admin_name'))
        {
            return redirect('admin/login');
        }
        DB::beginTransaction();
        try {
            $data = User::find($id);
            $data->delete();
            DB::commit();
            return redirect('admin/viewuser')->with('success','Data Delete..!');
        } catch (\Throwable $th) {
            DB::rollBack();
            return redirect('admin/viewuser')->with('error','Try Again..!'.$th->getMessage());
        }
    }
